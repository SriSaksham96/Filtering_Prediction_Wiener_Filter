#!/usr/bin/env python3
"""
Wiener Filter Reference Implementation
=======================================
Course : Computer Architecture Lab (CO2008)
Purpose: Independent Python verification of MIPS Wiener filter output.

This script implements the exact same algorithm as the MIPS assembly:
  1. Read desired.txt and input.txt
  2. Validate signal sizes
  3. Compute autocorrelation  r_xx(k) = (1/N) * sum x(n)*x(n-k)
  4. Build Toeplitz matrix R_M
  5. Compute cross-correlation gamma_d(k) = (1/N) * sum d(n)*x(n-k)
  6. Solve R_M * h = gamma_d via Gaussian elimination (with partial pivoting)
  7. Generate output signal y(n) using causal FIR convolution
  8. Compute MMSE = (1/N) * sum( (d(n) - y(n))^2 )
  9. Print and write results in the same format as the MIPS program

Note on precision: The MIPS program uses 32-bit single-precision floats,
while Python uses 64-bit double-precision. This causes minor rounding
differences (typically ±0.1 in formatted output). The reference output
files were generated by the grader's MIPS code using single-precision,
so exact bit-for-bit match is not expected from this Python script.

Usage:
  python3 wiener_filter_reference.py [input_file] [desired_file] [output_file]
  python3 wiener_filter_reference.py   (runs all test cases automatically)
"""

import sys
import os
import struct


def to_float32(value):
    """Convert a Python float to single-precision (float32) and back,
    simulating MIPS FPU precision."""
    return struct.unpack('f', struct.pack('f', value))[0]


def read_signal(filename):
    """Read whitespace-separated floating-point values from a text file."""
    with open(filename, 'r') as f:
        content = f.read()
    values = [float(x) for x in content.split()]
    return values


def compute_autocorrelation(x, M, N, use_float32=False):
    """
    Compute biased autocorrelation:
        r_xx(k) = (1/N) * sum_{n=k}^{N-1} x(n) * x(n-k),  k = 0..M-1

    Biased estimator divides by N (not N-k) to match the grader's implementation.
    """
    r_xx = []
    for k in range(M):
        total = 0.0
        for n in range(k, N):
            product = x[n] * x[n - k]
            if use_float32:
                product = to_float32(product)
            total += product
            if use_float32:
                total = to_float32(total)
        val = total / N
        if use_float32:
            val = to_float32(val)
        r_xx.append(val)
    return r_xx


def build_toeplitz(r_xx, M):
    """
    Build M x M Toeplitz matrix from autocorrelation values.
        R_M[i][j] = r_xx[|i - j|]
    """
    R = []
    for i in range(M):
        row = []
        for j in range(M):
            row.append(r_xx[abs(i - j)])
        R.append(row)
    return R


def compute_cross_correlation(d, x, M, N, use_float32=False):
    """
    Compute biased cross-correlation:
        gamma_d(k) = (1/N) * sum_{n=k}^{N-1} d(n) * x(n-k),  k = 0..M-1
    """
    gamma = []
    for k in range(M):
        total = 0.0
        for n in range(k, N):
            product = d[n] * x[n - k]
            if use_float32:
                product = to_float32(product)
            total += product
            if use_float32:
                total = to_float32(total)
        val = total / N
        if use_float32:
            val = to_float32(val)
        gamma.append(val)
    return gamma


def gaussian_elimination(R, gamma, M, use_float32=False):
    """
    Solve R * h = gamma using Gaussian elimination with partial pivoting.
    Returns the coefficient vector h.
    """
    # Build augmented matrix [R | gamma]  (M x M+1)
    aug = []
    for i in range(M):
        row = list(R[i]) + [gamma[i]]
        aug.append(row)

    # Forward elimination
    for pivot in range(M):
        # Partial pivoting
        best_row = pivot
        best_val = abs(aug[pivot][pivot])
        for r in range(pivot + 1, M):
            if abs(aug[r][pivot]) > best_val:
                best_val = abs(aug[r][pivot])
                best_row = r

        if best_row != pivot:
            aug[pivot], aug[best_row] = aug[best_row], aug[pivot]

        # Normalize pivot row
        pivot_val = aug[pivot][pivot]
        for c in range(pivot, M + 1):
            aug[pivot][c] /= pivot_val
            if use_float32:
                aug[pivot][c] = to_float32(aug[pivot][c])

        # Eliminate below
        for r in range(pivot + 1, M):
            factor = aug[r][pivot]
            for c in range(pivot, M + 1):
                aug[r][c] -= factor * aug[pivot][c]
                if use_float32:
                    aug[r][c] = to_float32(aug[r][c])

    # Back substitution
    h = [0.0] * M
    for row in range(M - 1, -1, -1):
        s = aug[row][M]
        for c in range(row + 1, M):
            s -= aug[row][c] * h[c]
            if use_float32:
                s = to_float32(s)
        h[row] = s

    return h


def generate_output_signal(h, x, M, N, use_float32=False):
    """
    Generate output signal using causal FIR convolution:
        y(n) = sum_{k=0}^{M-1} h(k) * x(n-k),  x(n-k) = 0 if n-k < 0
    """
    y = []
    for n in range(N):
        total = 0.0
        for k in range(M):
            if n - k >= 0:
                product = h[k] * x[n - k]
                if use_float32:
                    product = to_float32(product)
                total += product
                if use_float32:
                    total = to_float32(total)
        y.append(total)
    return y


def compute_mmse(d, y, N, use_float32=False):
    """
    Compute MMSE = (1/N) * sum_{n=0}^{N-1} (d(n) - y(n))^2
    """
    total = 0.0
    for n in range(N):
        error = d[n] - y[n]
        if use_float32:
            error = to_float32(error)
        sq = error * error
        if use_float32:
            sq = to_float32(sq)
        total += sq
        if use_float32:
            total = to_float32(total)
    result = total / N
    if use_float32:
        result = to_float32(result)
    return result


def format_float_1dp(value):
    """
    Format a float to 1 decimal place with half-up rounding,
    matching the MIPS float_to_str routine.
    """
    if value < 0:
        sign = "-"
        value = -value
    else:
        sign = ""
    n = int(value * 10 + 0.5)
    int_part = n // 10
    frac_part = n % 10
    return f"{sign}{int_part}.{frac_part}"


def run_wiener_filter(input_file, desired_file, output_file, use_float32=False):
    """
    Run the complete Wiener filter pipeline.
    """
    if not os.path.exists(desired_file):
        print(f"Warning: {desired_file} not found")
        return None
    if not os.path.exists(input_file):
        print(f"Error: {input_file} not found")
        return None

    d = read_signal(desired_file)
    x = read_signal(input_file)

    # Validate sizes
    if len(d) != len(x):
        error_msg = "Error: size not match"
        print(error_msg)
        with open(output_file, 'w') as f:
            f.write(error_msg + "\n")
        return "error"

    N = len(x)
    M = N  # Filter length = signal length

    # Full pipeline
    r_xx = compute_autocorrelation(x, M, N, use_float32)
    R = build_toeplitz(r_xx, M)
    gamma = compute_cross_correlation(d, x, M, N, use_float32)
    h = gaussian_elimination(R, gamma, M, use_float32)
    y = generate_output_signal(h, x, M, N, use_float32)
    mmse_val = compute_mmse(d, y, N, use_float32)

    # Format output
    y_formatted = " ".join(format_float_1dp(v) for v in y)
    mmse_formatted = format_float_1dp(mmse_val)
    output_str = f"Filtered output: {y_formatted}\nMMSE: {mmse_formatted}\n"

    print(output_str, end="")

    with open(output_file, 'w') as f:
        f.write(output_str)

    return output_str, y, mmse_val, h


def validate(test_name, computed_output, expected_file):
    """Compare computed output against reference output file."""
    if not os.path.exists(expected_file):
        print(f"  [SKIP] {expected_file} not found")
        return

    with open(expected_file, 'r') as f:
        expected = f.read().strip()

    computed = computed_output.strip()

    if computed == expected:
        print(f"  [PASS] {test_name}: Output matches reference exactly")
    else:
        # Check per-value closeness
        comp_vals = computed.replace("Filtered output: ", "").replace("\nMMSE: ", " ").split()
        exp_vals = expected.replace("Filtered output: ", "").replace("\nMMSE: ", " ").split()

        if len(comp_vals) == len(exp_vals):
            max_diff = 0
            for cv, ev in zip(comp_vals, exp_vals):
                try:
                    max_diff = max(max_diff, abs(float(cv) - float(ev)))
                except ValueError:
                    max_diff = 999
            if max_diff <= 0.2:
                print(f"  [CLOSE] {test_name}: Max difference = {max_diff:.1f} (within float32 rounding)")
            else:
                print(f"  [DIFF] {test_name}: Max difference = {max_diff:.1f}")
        else:
            print(f"  [FAIL] {test_name}: Different format")

        print(f"    Expected: {expected}")
        print(f"    Got:      {computed}")


def main():
    base_dir = os.path.dirname(os.path.abspath(__file__))

    if len(sys.argv) >= 4:
        run_wiener_filter(sys.argv[1], sys.argv[2], sys.argv[3])
        return

    # Run all test cases
    desired_file = os.path.join(base_dir, "desired.txt")

    test_cases = [
        ("Test 1", "input.txt",  "output_py_1.txt", "output.txt"),
        ("Test 2", "input2.txt", "output_py_2.txt", "output_2.txt"),
        ("Test 3", "input3.txt", "output_py_3.txt", "output_3.txt"),
    ]

    print("=" * 65)
    print("  Wiener Filter — Python Reference Validation")
    print("=" * 65)

    for precision_label, use_f32 in [("Double precision (Python default)", False),
                                       ("Single precision (simulated float32)", True)]:
        print(f"\n{'─'*65}")
        print(f"  Mode: {precision_label}")
        print(f"{'─'*65}")

        for test_name, inp, out, ref in test_cases:
            input_path = os.path.join(base_dir, inp)
            output_path = os.path.join(base_dir, out)
            ref_path = os.path.join(base_dir, ref)

            print(f"\n  --- {test_name}: {inp} ---")
            result = run_wiener_filter(input_path, desired_file, output_path, use_f32)

            if result and result != "error":
                output_str, y, mmse_val, h = result
                validate(test_name, output_str, ref_path)

    # Test 4: Size mismatch error
    print(f"\n{'─'*65}")
    print(f"  Error Case: Size Mismatch")
    print(f"{'─'*65}")
    mismatch_file = os.path.join(base_dir, "test_mismatch.txt")
    with open(mismatch_file, 'w') as f:
        f.write("1.0 2.0 3.0\n")
    mismatch_output = os.path.join(base_dir, "output_py_mismatch.txt")
    result = run_wiener_filter(mismatch_file, desired_file, mismatch_output)
    if result == "error":
        print("  [PASS] Size mismatch correctly detected")
    if os.path.exists(mismatch_file):
        os.remove(mismatch_file)

    print(f"\n{'='*65}")
    print("  Validation complete.")
    print(f"{'='*65}")


if __name__ == "__main__":
    main()
